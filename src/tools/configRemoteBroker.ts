function generateGitHubActionsConfig(
  brokerUrl: string,
  analysis: AnalysisResult
): string {
  const buildCommand =
    analysis.buildTool === "gradle"
      ? "./gradlew runContractTests"
      : "mvn test -Dtest=*PactTest";

  const publishCommand =
    analysis.buildTool === "gradle"
      ? "./gradlew pactPublish"
      : "mvn pact:publish";

  return `# Pact Contract Testing Workflow
# Generated by pact-contract-mcp
# Author: Sakthikannan Subramanian
#
# This workflow runs Pact contract tests and publishes results to the broker

name: Pact Contract Tests

on:
  push:
    branches: [main, develop]
  pull_request:
    branches: [main]

env:
  PACT_BROKER_URL: \${{ secrets.PACT_BROKER_URL }}
  PACT_BROKER_TOKEN: \${{ secrets.PACT_BROKER_TOKEN }}

jobs:
  consumer-contract-tests:
    name: Consumer Contract Tests
    runs-on: ubuntu-latest
    
    steps:
      - name: Checkout code
        uses: actions/checkout@v4
        
      - name: Set up JDK
        uses: actions/setup-java@v4
        with:
          java-version: '17'
          distribution: 'temurin'
          
      - name: Cache dependencies
        uses: actions/cache@v4
        with:
          path: |
            ~/.gradle/caches
            ~/.m2/repository
          key: \${{ runner.os }}-deps-\${{ hashFiles('**/*.gradle*', '**/pom.xml') }}
          restore-keys: |
            \${{ runner.os }}-deps-
            
      - name: Run Consumer Pact Tests
        run: ${buildCommand}
        env:
          GIT_COMMIT: \${{ github.sha }}
          GIT_BRANCH: \${{ github.ref_name }}
          
      - name: Publish Pact Contracts
        if: github.event_name == 'push'
        run: ${publishCommand}
        env:
          GIT_COMMIT: \${{ github.sha }}
          GIT_BRANCH: \${{ github.ref_name }}

  provider-contract-tests:
    name: Provider Contract Tests
    runs-on: ubuntu-latest
    needs: consumer-contract-tests
    if: github.event_name == 'push'
    
    steps:
      - name: Checkout code
        uses: actions/checkout@v4
        
      - name: Set up JDK
        uses: actions/setup-java@v4
        with:
          java-version: '17'
          distribution: 'temurin'
          
      - name: Cache dependencies
        uses: actions/cache@v4
        with:
          path: |
            ~/.gradle/caches
            ~/.m2/repository
          key: \${{ runner.os }}-deps-\${{ hashFiles('**/*.gradle*', '**/pom.xml') }}
          restore-keys: |
            \${{ runner.os }}-deps-
            
      - name: Verify Provider Contracts
        run: ${analysis.buildTool === "gradle" ? "./gradlew verifyPacts" : "mvn pact:verify"}
        env:
          GIT_COMMIT: \${{ github.sha }}
          GIT_BRANCH: \${{ github.ref_name }}

  can-i-deploy:
    name: Can I Deploy?
    runs-on: ubuntu-latest
    needs: [consumer-contract-tests, provider-contract-tests]
    if: github.event_name == 'push'
    
    steps:
      - name: Check deployment safety
        uses: pactflow/actions/can-i-deploy@v1
        with:
          broker_url: \${{ secrets.PACT_BROKER_URL }}
          broker_token: \${{ secrets.PACT_BROKER_TOKEN }}
          application_name: \${{ github.event.repository.name }}
          version: \${{ github.sha }}
          to_environment: production
          retry_while_unknown: 12
          retry_interval: 10
`;
}
